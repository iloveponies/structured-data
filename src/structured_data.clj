(defn author-count [book]
  (count (:authors book)))

(defn multiple-authors? [book]
  (> (author-count book)
     1))

(defn add-author [book new-author]
  (assoc book
         :authors
         (conj (:authors book)
               new-author)))

(defn alive? [author]
